def build_description(grade_num, domain_key):
    """Собирает markdown-описание навыков для данного грейда и домена."""
    lines = []

    # Заголовок грейда
    lines.append(f'## Грейд {grade_num} — {GRADE_DESCRIPTIONS[grade_num]}')
    lines.append('')

    # Задачи и инструмент
    tasks_info = GRADE_TASKS[domain_key][grade_num]
    lines.append(f'**Типовые задачи:** {tasks_info["tasks"]}')
    lines.append('')
    lines.append(f'**Инструмент:** {tasks_info["tools"]}')
    lines.append('')

    # Общие компетенции
    general = GENERAL_SKILLS.get(grade_num, [])
    if general:
        lines.append('### Общие компетенции')
        lines.append('')
        for skill_id, skill_text, confirmation in general:
            lines.append(f'- **{skill_id}** {skill_text} _{confirmation}_')
        lines.append('')

    # Профильные навыки
    domain_title = DOMAIN_TITLES[domain_key]
    domain_skills = DOMAIN_SKILLS[domain_key].get(grade_num, [])
    if domain_skills:
        lines.append(f'### {domain_title}')
        lines.append('')
        for skill_id, skill_text, confirmation in domain_skills:
            lines.append(f'- **{skill_id}** {skill_text} _{confirmation}_')
        lines.append('')

    # Навыки предыдущих грейдов (кумулятивность)
    if grade_num > 1:
        lines.append(f'---')
        lines.append(f'*Включает все навыки грейдов 1–{grade_num - 1}*')

    return '\n'.join(lines)


class Command(BaseCommand):
    help = 'Заполняет навыки разрядов (WorkerGradeSkills) из матрицы компетенций'

    def add_arguments(self, parser):
        parser.add_argument(
            '--clear',
            action='store_true',
            help='Очистить существующие навыки перед заполнением',
        )

    def handle(self, *args, **options):
        if options['clear']:
            count = WorkerGradeSkills.objects.all().count()
            WorkerGradeSkills.objects.all().delete()
            self.stdout.write(f'Очищено навыков: {count}')

        # Получаем разряды
        grades = {g.grade: g for g in WorkerGrade.objects.all()}
        if not grades:
            self.stderr.write(self.style.ERROR(
                'Разряды не найдены. Сначала запустите: python manage.py populate_pricelists'
            ))
            return

        # Получаем корневые разделы
        root_sections = WorkSection.objects.filter(parent__isnull=True, is_active=True)
        if not root_sections.exists():
            self.stderr.write(self.style.ERROR(
                'Корневые разделы работ не найдены. Сначала импортируйте прайс-лист.'
            ))
            return

        created_count = 0
        updated_count = 0

        for section in root_sections:
            # Определяем домен по имени раздела
            domain_key = None
            section_name_lower = section.name.lower()
            for keyword, domain in SECTION_DOMAIN_MAP.items():
                if keyword in section_name_lower:
                    domain_key = domain
                    break

            if not domain_key:
                self.stdout.write(self.style.WARNING(
                    f'  Раздел «{section.name}» не сопоставлен с доменом — пропущен'
                ))
                continue

            self.stdout.write(f'\nРаздел: {section.name} → {DOMAIN_TITLES[domain_key]}')

            for grade_num in range(1, 6):
                grade_obj = grades.get(grade_num)
                if not grade_obj:
                    continue

                description = build_description(grade_num, domain_key)

                obj, created = WorkerGradeSkills.objects.update_or_create(
                    grade=grade_obj,
                    section=section,
                    defaults={'description': description},
                )

                status = 'создан' if created else 'обновлён'
                if created:
                    created_count += 1
                else:
                    updated_count += 1
                self.stdout.write(f'  Грейд {grade_num}: {status}')

        self.stdout.write(self.style.SUCCESS(
            f'\nГотово! Создано: {created_count}, обновлено: {updated_count}'
        ))

class TestReplaceBasenameInHtml:
    def test_basic_img_src(self):
        html = '<p>X</p><img src="/media/news/old.png" alt="x">'
        out, n = replace_basename_in_html(html, "old.png", "new.png")
        assert n == 1
        assert "/media/news/new.png" in out
        assert "old.png" not in out

    def test_href_a_tag(self):
        html = '<a href="https://hvac-info.com/media/news/old.jpg">link</a>'
        out, n = replace_basename_in_html(html, "old.jpg", "new.jpg")
        assert n == 1
        assert "new.jpg" in out

    def test_css_url(self):
        html = '<div style="background: url(/media/news/old.png)"></div>'
        out, n = replace_basename_in_html(html, "old.png", "new.png")
        assert n == 1
        assert "url(/media/news/new.png)" in out

    def test_idempotent_same_basename(self):
        html = '<img src="/media/news/x.png">'
        out, n = replace_basename_in_html(html, "x.png", "x.png")
        assert n == 0
        assert out == html

    def test_no_replacements_when_basename_absent(self):
        html = '<img src="/media/news/foo.png">'
        out, n = replace_basename_in_html(html, "bar.png", "baz.png")
        assert n == 0
        assert out == html

    def test_does_not_touch_plain_text(self):
        # basename как plain-text (не в src/href/url()) не должен заменяться,
        # чтобы избежать ложных срабатываний.
        html = '<p>Файл old.png упомянут в тексте</p>'
        out, n = replace_basename_in_html(html, "old.png", "new.png")
        assert n == 0
        assert out == html

    def test_multiple_occurrences(self):
        html = (
            '<img src="/media/a/old.png">'
            '<img src="/media/b/old.png">'
        )
        out, n = replace_basename_in_html(html, "old.png", "new.png")
        assert n == 2
        assert out.count("new.png") == 2



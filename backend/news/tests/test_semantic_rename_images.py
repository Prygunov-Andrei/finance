@pytest.mark.django_db
class TestCommandShared:
    def test_shared_image_renamed_once(self, media_root, admin_user):
        """Картинка-«сирота», на которую ссылаются два поста, переименовывается
        ровно один раз внутри одного запуска команды; LLM вызван один раз;
        body обоих постов обновляется."""
        storage_name = "news/uploads/snimok_shared.png"
        default_storage.save(storage_name, SimpleUploadedFile("x.png", _png_bytes()))
        group = NewsDuplicateGroup.objects.create(
            merged_title="m", merged_body="", source_count=2,
        )
        body = f'<img src="/media/{storage_name}">'
        post_a = NewsPostFactory(
            title="Daikin запускает завод",
            body=body, duplicate_group=group,
        )
        post_b = NewsPostFactory(
            title="Daikin запускает завод (другой источник)",
            body=body, duplicate_group=group,
        )

        with patch("news.services_semantic_naming.NewsLLMClient.query_raw") as mock_q:
            mock_q.return_value = "daikin-zavod-launch"
            # Без --post-id — пройдёт по обоим постам в одном запуске.
            call_command(
                "semantic_rename_images",
                "--execute", "--sleep", "0",
                stdout=StringIO(),
            )
        # LLM вызван ровно один раз — второй пост увидел картинку в rename_map.
        assert mock_q.call_count == 1
        # Файл существует под новым именем.
        new_path = "news/uploads/daikin-zavod-launch.png"
        assert default_storage.exists(new_path)
        # Старого файла нет.
        assert not default_storage.exists(storage_name)
        # Оба body обновились.
        post_a.refresh_from_db()
        post_b.refresh_from_db()
        assert "daikin-zavod-launch.png" in post_a.body
        assert "daikin-zavod-launch.png" in post_b.body

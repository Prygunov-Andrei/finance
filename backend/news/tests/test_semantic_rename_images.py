class TestPlanNewStorageName:
    def test_basic(self, media_root):
        # Без коллизии — просто dirname/slug.ext.
        assert (
            plan_new_storage_name(
                "news/uploads/2026/02/snimok_x.png", "novyi-news", default_storage
            )
            == "news/uploads/2026/02/novyi-news.png"
        )

    def test_no_dirname(self, media_root):
        assert (
            plan_new_storage_name("snimok.png", "x-y-z-w", default_storage)
            == "x-y-z-w.png"
        )

    def test_collision_with_existing_file(self, media_root):
        # Создаём «занятый» файл на storage.
        default_storage.save(
            "news/uploads/foo.png",
            SimpleUploadedFile("foo.png", _png_bytes()),
        )
        # foo.png занят → должен подобрать foo-2.png.
        result = plan_new_storage_name(
            "news/uploads/snimok.png", "foo", default_storage
        )
        assert result == "news/uploads/foo-2.png"

    def test_collision_via_extra_taken(self, media_root):
        # foo.png НЕ существует на disk, но «зарезервирован» в этом run-е.
        result = plan_new_storage_name(
            "news/uploads/snimok.png",
            "foo",
            default_storage,
            extra_taken={"news/uploads/foo.png"},
        )
        assert result == "news/uploads/foo-2.png"

    def test_same_old_path_returned_when_slug_matches(self, media_root):
        # Если новый path == старому — возвращаем как есть, без увеличения суффикса.
        assert (
            plan_new_storage_name(
                "news/uploads/foo.png", "foo", default_storage
            )
            == "news/uploads/foo.png"
        )



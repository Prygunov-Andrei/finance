class TestParseSlug:
    def test_clean_response(self):
        assert parse_slug("novyi-kompressor-danfoss") == "novyi-kompressor-danfoss"

    def test_underscore_to_dash(self):
        assert parse_slug("novyi_kompressor_danfoss") == "novyi-kompressor-danfoss"

    def test_strip_quotes(self):
        assert parse_slug('"novyi-kompressor"') == "novyi-kompressor"
        assert parse_slug("'rotational-compressor'") == "rotational-compressor"

    def test_strip_backticks(self):
        assert parse_slug("`some-slug-here`") == "some-slug-here"

    def test_multiline_takes_first(self):
        assert parse_slug("danfoss-news\n\nObjasnenie") == "danfoss-news"

    def test_uppercase_lowered(self):
        assert parse_slug("NOVYI-KOMPRESSOR") == "novyi-kompressor"

    def test_collapse_dashes(self):
        assert parse_slug("foo--bar---baz") == "foo-bar-baz"

    def test_strip_leading_trailing_dash(self):
        assert parse_slug("-foo-bar-") == "foo-bar"

    def test_too_short_returns_none(self):
        assert parse_slug("abc") is None  # < 5 chars

    def test_too_long_returns_none(self):
        assert parse_slug("a" * 100) is None

    def test_empty_returns_none(self):
        assert parse_slug("") is None
        assert parse_slug(None) is None
        assert parse_slug("   ") is None

    def test_pure_cyrillic_after_clean_returns_none(self):
        # После strip non-latin останется пусто.
        assert parse_slug("кондиционер") is None

    def test_mixed_letters_strip_cyrillic(self):
        # «danfoss-новый» → «danfoss-» после удаления нелатиницы → «danfoss».
        # Это короче 5 символов? «danfoss» = 7 → ОК.
        assert parse_slug("danfoss-новый") == "danfoss"

    def test_rejects_punctuation_chars(self):
        # Slash, скобки и пробелы — обрезаются → итог должен пройти validate.
        assert parse_slug("foo/bar baz") == "foo-bar-baz"



class TestUrlToStorageName:
    def test_relative_media_url(self):
        assert (
            url_to_storage_name("/media/news/uploads/2026/02/snimok_x.png")
            == "news/uploads/2026/02/snimok_x.png"
        )

    def test_relative_hvac_media_url(self):
        assert (
            url_to_storage_name("/hvac-media/news/media/foo.jpg")
            == "news/media/foo.jpg"
        )

    def test_absolute_url(self):
        assert (
            url_to_storage_name("https://hvac-info.com/media/news/X/y.png")
            == "news/X/y.png"
        )

    def test_percent_encoded(self):
        # %D0%A1%D0%BD%D0%B8%D0%BC%D0%BE%D0%BA = «Снимок»
        assert (
            url_to_storage_name(
                "/media/news/uploads/%D0%A1%D0%BD%D0%B8%D0%BC%D0%BE%D0%BA.png"
            )
            == "news/uploads/Снимок.png"
        )

    def test_non_media_url(self):
        assert url_to_storage_name("/static/img/logo.svg") is None

    def test_empty_url(self):
        assert url_to_storage_name("") is None
        assert url_to_storage_name(None) is None

    def test_url_with_query_string_stripped(self):
        assert (
            url_to_storage_name("/media/news/x.png?v=2")
            == "news/x.png"
        )


